
    <h1>Guest House Rules and Policies</h1>
    <h2>Eligibility:</h2>
    <ul>
        <li>Membership and Guest Eligibility: Members of the organization and their guests are eligible to stay at the PCOM Guest House.</li>
        <li>Maximum Booking Period: Rooms can be booked for a maximum period of 7 days. Extension beyond this duration may be granted solely at the discretion of the Assistant Secretary PCOM.</li>
        <li>Room Vacancy: Guests must vacate their rooms on the designated checkout date. Extensions may be considered only if there are no other reservations for the requested period, subject to approval by the Assistant Secretary.</li>
        <li>Consequences of Rule Deviation: Any deviation from the rules may result in the guest being blacklisted from future bookings at the Guest House. Additionally, it will be considered a violation of the Mess Rules/Regulations, and disciplinary action may be taken by the Disciplinary Committee.</li>
    </ul>
    <h2>Booking Procedure:</h2>
    <ul>
        <li>Priority and Approval: Bookings are processed on a first-come, first-served basis and are subject to availability.</li>
        <li>Booking Hours: Bookings are not accepted between 11:00 PM and 8:00 AM.</li>
        <li>Check-in Procedure: Guests must fill out a check-in form in their handwriting, which must be duly signed.</li>
        <li>Checkout Time: The checkout time is 12:00 PM.</li>
        <li>Restrictions on Other Occupants: The Guest House does not accommodate drivers, guards, maids, or other servants of guests.</li>
        <li>Late Check-in Notification: Guests must inform in advance if they intend to check in late.</li>
        <li>Facility Usage: Guests are permitted to use the Gym & Swimming Pool facilities at per-visit charges, which are established by the officers' mess according to the member category.</li>
        <li>Room Change Request: Requests for room changes are considered only if no other reservations exist for the intended period, subject to approval by the Assistant Secretary.</li>
        <li>Cancellation and Modification Policy: Cancellation or booking changes incur charges as per approved rates.</li>
    </ul>
    <h2>Room Charges:</h2>
    <ul>
        <li>Additional Charges:
            <ul>
                <li>Extra mattress: Rs. 750 per person per night.</li>
                <li>Room rent is subject to change.</li>
                <li>Cancellation charges apply based on the time of cancellation.</li>
            </ul>
        </li>
        <li>Free Cancellation: Guests can cancel their reservation free of charge up to 48 hours before the scheduled check-in time.</li>
        <li>Partial Cancellation Fee: If cancellation occurs 24 hours before the check-in date, a partial cancellation fee of 25% of the room charges will be applied.</li>
        <li>Full Cancellation Fee: Cancellations made within 24 hours of the scheduled check-in time or in the case of a no-show will result in a full cancellation fee, with 100% of the room charges being charged to the member.</li>
    </ul>
    <h2>Payment:</h2>
    <ul>
        <li>Clearance of Bills: Guests must settle all outstanding bills in cash before checkout. Otherwise, the bill will be charged to the member.</li>
        <li>Liability: Members are responsible for any outstanding bills incurred by their guests.</li>
    </ul>
    <h2>Guest House Rules:</h2>
    <ol>
        <li>Guest Conduct: Guests are personally responsible for the behavior of their visitors.</li>
        <li>Visitation Hours: Visitors are not permitted in guest rooms after 10:00 PM.</li>
        <li>Prohibition of Parties: Hosting parties in guest rooms is strictly prohibited.</li>
        <li>Restricted Items: Pets, weapons, and heavy electrical equipment such as irons and heaters are not allowed in rooms.</li>
        <li>Limited Room Service: Room service is not available between 10:00 PM and 7:00 AM.</li>
        <li>Security of Belongings: The Guest House is not responsible for the loss of cash and valuables.</li>
        <li>Lost Key Fine: Guests will be fined Rs. 3000 for the loss of room keys.</li>
    </ol>
    <h2>Takeaway Policy for Officers' Mess Members:</h2>
    <ol>
        <li>Order Finalization: Once an order is placed by an Officers' Mess member, it is considered final and cannot be changed or canceled thereafter.</li>
        <li>Alterations and Cancellations: Members are advised to carefully review their orders before finalizing the transaction, as no changes or cancellations can be accommodated once the order is submitted.</li>
        <li>Pickup Timeframe: Orders must be picked up within a minimum of 45 minutes and a maximum of 2 hours from the time of order placement.</li>
        <li>Timely Pickup: It is the responsibility of the member to ensure they arrive promptly to pick up their order within the specified timeframe.</li>
        <li>Automatic Cancellation: If orders are not picked up within the designated 45-minute to 2-hour timeframe, they will be automatically canceled.</li>
        <li>Payment Consequences: In the event of order cancellation due to non-pickup, the order amount will be charged to the member's membership account.</li>
    </ol>

